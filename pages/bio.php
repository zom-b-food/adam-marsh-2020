                    <p>
                        Well, let's see. I gasped my first breath the Sunday evening of January 20,
                        19-something. Don't ask me my age unless you're prepared to believe a lie. I
                        was adopted as a baby, am the middle child of 3, grew up in Texas and
                        Indiana where I rode
                        motorcycles through corn fields, paddled canoes over waterfalls, dreamed of
                        being a poet--an expatriate living in a far and distant land. I went to
                        college in Utah and Texas and have a BA in Theatre and another one in
                        English (composition &amp;
                        rhetoric). I lived in New York City for a while and attended the National
                        Shakespeare Conservatory but dropped out after spending all of my
                        student-aid money on 2-months rent, a plate of eggs benedict and a
                        basketball. I know, crazy, right? Why did I
                        buy a
                        <span class="em">basketball</span>?
                    </p>

