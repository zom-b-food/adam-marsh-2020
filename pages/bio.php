                    <p>I worked as a technical editor for a publishing company in NYC. Recently
                        married, we lived in a tiny "artsy" (meaning mouse and roach infested)
                        studio in Gramercy Park. A couple of years later, our beautiful daughter was
                        born and we moved to Texas
                        where I went to work for its democratic governor. And suddenly I discovered
                        that I was a closet socialist and then summarily &quot;fired&quot; when Bush
                        came to town. I spent my early career looking for a career. Spent my
                        mid-career trying to
                        resuscitate my first career and soon thereafter settled into my current
                        career that felt like coming to after falling down the balcony stairs. I
                        love what I do and do it quite well. Initially, however, I wanted to write
                        novels on a beach in Northern
                        Africa or make cheese out of a wagon while traveling with gypsies, but I was
                        pressed into a real job due to the above mentioned child and a delinquent
                        student loan.</p>

                    <p>Anyway, I married a great girl before I was ready, before I purged myself of
                        those delusions we endured in the early to mid-90's. We had a great kid
                        amidst the fallout of denial, anger, and acceptance that these delusions
                        endowed us with. So it
                        was imminent that divorce would inevitably follow, that this train I was
                        riding was approaching a dark tunnel to oblivion--a wormhole into the cold
                        isolation of insensible space. It was at that moment I was hit with a
                        <span class="em">life brick</span>, as I call it, and knocked from the
                        mystery train to travel a parallel path while my injuries healed themselves
                        over time and distance, a realization that only now can I perceive more
                        fully.</p>

                    <p><img style="float:left; padding-right:6px;" src="/assets/images/rune1.jpg"
                            width="35" height="58"/>
                        <img style="float:right; padding-left:6px;" src="/assets/images/us1.jpg"
                             width="216" height="151"/>I've always believed that life is a journey
                        as depicted by the tiny rune tattooed on my hand. Some go through life
                        proclaiming no
                        regrets. Whatever. In my view, life is about regret. Without it, where's the
                        growth? How is there self-realization in a context that transcends the here
                        and now, that leads to a greater understanding of the human condition and
                        our place within it?
                        Today I aim to journey on without the pathos of a martyr looking over his
                        shoulder at an image blurred by age and remorse. I try to guide my life by
                        that tiny voice within me struggling each day to sever myself from ego and
                        impetuism...(not sure if
                        that's a word, but...). I hope to obtain someday the wise dignity of an
                        artist or a scholar; always pressing forward like
                        <span class="em">everyman</span>--going to work, paying the bills, feeding
                        the family, reasoning his way through days that go on and on like rain
                        dripping down a mountain.</p>

                    <p>But, gratefully, I don't spend my days gasping for breath like I did
                        40-something years ago. I own up to a few regrets like ordering 4 star Duck
                        Pou at Racha down the hill, but the grass is green and the air is fresh and
                        crisp.
                    </p>

