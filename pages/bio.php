                <p>I've realized recently that the story of my life is really an amalgamation of
                    careful planning and improvisation. Whether I got to this point today by
                    sticking to a plan or going with the flow, who I am now has got me thinking
                    about who I was
                    yesterday. And so in the middle of the night, I stumble into the kitchen and
                    knock down a photo of grandma while fumbling for the light switch. I improvise
                    as though I'm in a strange new place. And in the darkness I reach out for gram's
                    picture
                    guided only by intuition. Our histories are like footprints on a map--a record
                    of our past dotted with a plethora of what ifs. In spite of my best efforts and
                    preparations, much of my life has been a quirksome surprise. So in light of this
                    realization, I will try to lay out a short bio hoping to bridge the gap between
                    <em>planned</em>
                    events and
                    <em>what the hell just happened</em>
                    events.
                </p>
